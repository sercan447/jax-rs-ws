package com.istanbul.pojo;

public class Bankalar {

	private String subeadi;
	private String bankaismi;
	
	
	public Bankalar() {
		
	}
	
	public Bankalar(String subeadi, String bankaismi) {
		
		this.subeadi = subeadi;
		this.bankaismi = bankaismi;
	}
	public String getSubeadi() {
		return subeadi;
	}
	public void setSubeadi(String subeadi) {
		this.subeadi = subeadi;
	}
	public String getBankaismi() {
		return bankaismi;
	}
	public void setBankaismi(String bankaismi) {
		this.bankaismi = bankaismi;
	}
	
	
	
	
	
}
